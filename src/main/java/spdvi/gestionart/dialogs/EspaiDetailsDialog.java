    private void btnCommentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCommentActionPerformed
        DataAccess da = new DataAccess();
        SessionSingleton session = SessionSingleton.INSTANCE.getInstance();
        
        int result = da.insertComentariEspai(txtNewComment.getText(),
                session.getUsuari().getId(), espai.getRegistre(),
                Integer.parseInt(cmbValoracio.getSelectedItem().toString()));
        loadComments();
    }//GEN-LAST:event_btnCommentActionPerformed

    private void loadImages() {
        DataAccess da = new DataAccess();
        txtModalitats.setText(da.getModalitatsEspai(espai.getRegistre()).toString());
        txtServeis.setText(da.getServeisEspai(espai.getRegistre()).toString());
        ArrayList<byte[]> imatges = da.getImatgesEspai(espai.getRegistre());
        for (byte[] binaryImage : imatges) {
            ImageIcon imatge = new ImageIcon(binaryImage);
            JLabel lblImatge = new JLabel();
            lblImatge.setText("");
            lblImatge.setSize(Constants.DEFAULT_PROFILE_IMAGE_WIDTH, Constants.DEFAULT_PROFILE_IMAGE_HEIGHT);
            lblImatge.setIcon(imatge);
            pnlImatges.add(lblImatge);
        }
        pack();
    }

    private void loadComments() {
        DataAccess da = new DataAccess();
        ArrayList<Comentari> comentaris = da.getComentarisEspai(espai.getRegistre());
        
        for(Comentari comentari: comentaris) {
            ComentariComponent comentariComponent = new ComentariComponent();
            comentariComponent.setComentari(comentari);
            pnlComentaris.add(comentariComponent);
        }

//        txtComentaris.setText("");
//        for (String comentari: comentaris) {
//            txtComentaris.append(comentari + "\n\n");
//        }
    }
    
    private void loadValoracioAvg() {
        DataAccess da = new DataAccess();
        float valoracioAvg = da.getValoracioEspaiAvg(espai.getRegistre());
        lblValoracio.setText("Valoraci√≥ mitjana: " + String.format("%.1f", valoracioAvg));
    }
